def generate_slug(length: int = 8) -> str:
    return "".join(secrets.choice(ALPHABET) for _ in range(length))


def generate_names(separator: str = "-") -> str:
    name = []
    for word in (ADJECTIVES, SCIENTIST_LAST_NAMES):
        part = None
        while not part or part in name:
            part = random.choice(word)
        name.append(part.lower())
    return separator.join(name)
